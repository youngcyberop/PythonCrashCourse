# Changing case in a string with methods

name = "ada lovelace"
print(name.title())

#   name is the variable
#   print is a function
#   title is a method
#   name.title passes the name variable to the title method to change case
#   Additional parameters cna be passed to the method inside the brackets

# Other useful methods

# add output space
print("")

name = "Ada LoveLace"
#   upper method capitalizes all characters in a string
print(name.upper())
#   lower() method changes all case to lower case in a string
#   this can be important when storing data
print(name.lower())

